package exemplos;

public class Exemplo01 {
    public static void main(String[] args) {
        int cont;

        cont=1; // inicialização
        while (cont <= 10 ){ //teste de fim
            System.out.println(cont);
            cont++; // contador mesma coisa que cont = cont +1 
            // para fazer o passo maior que um cont= cont +2 (exemplo)
            // Abreviacao de cont = cont + 2 ----------- cont += 2
        }
    }
    
}
