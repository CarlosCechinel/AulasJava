public class Exemplo01 {

    public static void main(String[] args) { 
        System.out.println("Olá mundo");
        System.out.println("Primeiro programa"); // \n é quebra de linha

        System.out.println("2 + 3");
        System.out.println(2 + 3);

        System.out.println("Resposta =" + 2 + 3);
        System.out.println("Resposta = " + (2+3)); // () altera a prioridade de execução

        System.out.println("Quanto é 2 + 3 ?" + "\nResposta = " + (2+3));
    }
}

